package com.Bop_Dop.Patients;

public class Patient_model
{
	private long patient_id;
	private String patient_name;
	private String age;
	private String gender;
	private String email;
	private String address;
	private String mob_number;
	private String city;
	private String state;
	/**
	 * @return the patient_id
	 */
	public long getPatient_id() {
		return patient_id;
	}
	/**
	 * @param patient_id the patient_id to set
	 */
	public void setPatient_id(long patient_id) {
		this.patient_id = patient_id;
	}
	/**
	 * @return the patient_name
	 */
	public String getPatient_name() {
		return patient_name;
	}
	/**
	 * @param patient_name the patient_name to set
	 */
	public void setPatient_name(String patient_name) {
		this.patient_name = patient_name;
	}
	/**
	 * @return the age
	 */
	public String getAge() {
		return age;
	}
	/**
	 * @param age the age to set
	 */
	public void setAge(String age) {
		this.age = age;
	}
	/**
	 * @return the gender
	 */
	public String getGender() {
		return gender;
	}
	/**
	 * @param gender the gender to set
	 */
	public void setGender(String gender) {
		this.gender = gender;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	/**
	 * @return the mob_number
	 */
	public String getMob_number() {
		return mob_number;
	}
	/**
	 * @param mob_number the mob_number to set
	 */
	public void setMob_number(String mob_number) {
		this.mob_number = mob_number;
	}
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
}